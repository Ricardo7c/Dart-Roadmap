void main(){
  var diaSemana = 3;

  switch (diaSemana){
    case 1:
      print("Domingo");
    case 2:
      print("Hoje é segunda-feira");
    case 3:
      print("Hoje é terça-feira");
    case 4:
      print("Hoje é quarta-feira");
    case 5:
      print("Hoje é quinta-feira");
    case 6:
      print("Hoje é sexta-feira");
    case 7:
      print("hoje é sabado");
    default:
      print("Opção inválida");
  }
}